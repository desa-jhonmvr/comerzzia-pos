    @Override
    public void iniciaVista() {
        log.info("Inicia Vista");
        contenedor.setModalExclusionType(ModalExclusionType.APPLICATION_EXCLUDE);
        contenedor.setModalityType(ModalityType.APPLICATION_MODAL);
        refrescaTabla();
        
        if (!reserva.getReservacion().getReservaInvitadoList().isEmpty()){
            if (tb_invitados.getRowCount() >= 0) {
                    ListSelectionModel selectionModel = tb_invitados.getSelectionModel();
                    selectionModel.setSelectionInterval(0, 0);
                    tb_invitados.requestFocus();
                }
            tb_invitados.requestFocus();
            
        }
        else{
            b_cancelar5.requestFocus();
        }
        
        if(!reserva.getReservacion().getCodalm().equals(Sesion.getTienda().getCodalm())){
            b_anadirInvitado.setEnabled(false);
        }
    }

