private void b_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_okActionPerformed
    try {


        for (IValidableForm iv : formulario) {
            iv.validar();
        }

        BigInteger idInvitado = new BigInteger(ReservacionesServicios.consultaSiguienteIdInvitado(reserva.getReservacion().getUidReservacion()).toString());
        ReservaInvitadoBean invitado = new ReservaInvitadoBean(reserva.getReservacion().getUidReservacion(), idInvitado.longValue());
        invitado.setNombre(t_invitado_nombre.getText());
        invitado.setApellido(t_invitado_apellidos.getText());
        invitado.setEmail(t_invitado_email.getText());
        invitado.setTelefono(t_invitado_telefono.getText());
        invitado.setReserva(reserva.getReservacion());
        invitado.setProcesadoTienda(false);

        reserva.setInvitadoSeleccionado(invitado);
        reserva.getReservacion().getReservaInvitadoList().add(invitado);
        ReservacionesServicios.crearInvitado(invitado);
        contenedor.setVisible(false);
        ventana_padre.getContenedor().setVisible(false);


    }
    catch (ValidationFormException e){
        // El error ya se debe pintar en pantalla
    }
    catch (IndexOutOfBoundsException e) {
        ventana_padre.getVentana_padre().crearAdvertencia("Debe seleccionar un invitado para aceptar. ");
    }
    catch (Exception e) {
        log.error(e);
        ventana_padre.getVentana_padre().crearError(e.getMessage());
    }
}//GEN-LAST:event_b_okActionPerformed

