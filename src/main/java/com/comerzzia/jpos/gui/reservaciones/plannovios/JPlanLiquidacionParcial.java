private void tb_articulosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tb_articulosKeyPressed
    if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
        int indiceSeleccionado = -1;
        try {
            indiceSeleccionado = tb_articulos.getSelectedRow();
            manejador.accionSeleccionaArticulo(indiceSeleccionado);
            lb_error.setText("");
        }
        catch (PlanNovioException e) {
            lb_error.setText(e.getMessage());
        }
        t_articulos_seleccionados.setText(manejador.getSeleccionados().toString());
        t_restante_abonos.setText(manejador.getRestante().toString());
        refrescarTabla(indiceSeleccionado);
    }
    else if (evt.getKeyCode() == KeyEvent.VK_TAB && evt.isShiftDown()) {
        tb_articulos.transferFocusBackward();
    }
    else if (evt.getKeyCode() == KeyEvent.VK_TAB) {
        tb_articulos.transferFocus();
    }
}//GEN-LAST:event_tb_articulosKeyPressed
