    private void refrescarTabla(final int indiceSeleccionado) {
        if (manejador.getPlanNovio().getArticulosSeleccionados().isEmpty()) {
            b_ok.setEnabled(false);
        }
        else {
            b_ok.setEnabled(true);
        }
        LiquidacionPlanTableModel modelo = new LiquidacionPlanTableModel(manejador.getPlanNovio().getArticulosNoComprados());
        tb_articulos.setModel(modelo);
        if (tb_articulos.getRowCount() >= 0) {
            ListSelectionModel selectionModel = tb_articulos.getSelectionModel();
            
            SwingUtilities.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        if (indiceSeleccionado >= 0) {
                            tb_articulos.addRowSelectionInterval(indiceSeleccionado, indiceSeleccionado);
                        }
                    }
                });
            
            
            tb_articulos.requestFocus();
        }
        else {
            b_cancelar.requestFocus();
        }
    }

