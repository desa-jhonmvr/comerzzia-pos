    @Override
    public void accionAceptar() {
        log.info("Acción Aceptar");
        try {          
            validaFormulario();
            this.getContenedor().setVisible(false);
            //this.pasarelaAPagos.crearVentanaPagos(new BigDecimal(t_importe.getText())); No usamos la ventana
            importeAAbonar= new BigDecimal(t_importe.getText());
            log.debug("Importe seleccionado para abonar: "+importeAAbonar.toString());                    
        }
        catch (ValidationFormException ex) {
            // No hace falta mostrar el mensaje puesto que el validador ya lo saca a a etiqueta de errores
            log.debug("El importe introducido " + t_importe.getText() + "No ha superado la validación");
        }
    }

