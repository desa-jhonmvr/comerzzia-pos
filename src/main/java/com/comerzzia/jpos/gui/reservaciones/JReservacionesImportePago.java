    public JReservacionesImportePago(BigDecimal valorMinimoE, BigDecimal valorMaximoE) {
        log.info("CONSTRUCTOR DE IMPORTE DE PAGOS");
        log.info("Valor Mínimo "+ valorMinimoE.toString());
        log.info("Valor Máximo "+ valorMaximoE.toString());
        
        this.valorMinimo = valorMinimoE;
        this.valorMaximo = valorMaximoE;
        initComponents();
        registraEventoEnterBoton();
        t_importe.addValidador(new ValidadorObligatoriedad(), this);
        if (valorMinimo == null && valorMaximo == null) {
            t_importe.addValidador(new ValidadorDecimal(), this);
            lb_cantidad_maxima.setText("");
            lb_cantidad_minima.setText("");
        }
        else if (valorMinimo != null && valorMaximo == null) {
            t_importe.addValidador(new ValidadorDecimal(null, valorMinimo, 2), this);
            lb_cantidad_maxima.setText("");
            lb_cantidad_minima.setText("Abono mínimo  $ "+valorMinimo.toString());
        }
        else if (valorMinimo == null && valorMaximo != null) {
            t_importe.addValidador(new ValidadorDecimal(valorMaximo, BigDecimal.ZERO, 2), this);
            lb_cantidad_maxima.setText("Abono máximo  $ "+valorMaximo.toString());
            lb_cantidad_minima.setText("");
        }
        else {
            t_importe.addValidador(new ValidadorDecimal(valorMaximo, valorMinimo, 2), this);
            lb_cantidad_maxima.setText("Abono máximo  $ "+valorMaximo.toString());
            lb_cantidad_minima.setText("Abono mínimo  $ "+valorMinimo.toString());
            
        }

        clearError();


    }

