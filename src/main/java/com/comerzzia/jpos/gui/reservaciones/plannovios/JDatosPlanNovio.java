    private void inicializarValidacion() {
        
        t_lugar_boda.addValidador(new ValidadorObligatoriedad(), this);
        t_lugar_boda.addValidador(new ValidadorTexto(400), this);
        // validador de fecha/hora
        t_fecha_hora_boda.addValidador(new ValidadorObligatoriedad(), this);
        t_fecha_hora_boda.addValidador(new ValidadorFechaHora(), this);
        
        t_fecha_contacto_invitados.addValidador(new ValidadorFecha2(), this);
        t_telefono.addValidador(new ValidadorTelefono(), this);
        t_telefono.addValidador(new ValidadorBigInteger(), this);
        t_email.addValidador(new ValidadorEmail(), this);
    }

    private void inicializarFormulario() {
        formulario = new LinkedList<IValidableForm>();

        formulario.add(t_lugar_boda);
        formulario.add(t_fecha_hora_boda);
        formulario.add(t_fecha_contacto_invitados);
        formulario.add(t_telefono);
        formulario.add(t_email);
        
    }

    private void accionReservar() {
        try {
            log.debug("accionReservar()");
            log.debug("-- Validación");
            for (IValidableForm ivf : formulario) {
                ivf.validar();
            }
            manejador.establecerDatosAdicionales( t_lugar_boda, t_fecha_hora_boda, t_fecha_contacto_invitados, t_telefono, t_email);

            if (manejador.getPlanNovio().getModo() == PlanNovioOBJ.MODO_MODIFICANDO_PLAN) {
                // MODO MODIFICANDO
                log.debug("-- Modificando Plan");
                manejador.modificaPlan();
            }
            else {
                // MODO CREACION
                log.debug("-- Creando Plan");
                manejador.crearPlan();
            }

            ventana_padre.irMostrarPlanNovio();
        }
        catch (PlanNovioException ex) {
            log.error(ex.getMessage());
            addError(new ValidationFormException("Error guardando datos del Plan"));
        }
        catch (ValidationFormException ex) {
            lb_error.setText(ex.getMessage());
            log.debug("Errores de validación en el formulario");
            addError(ex);
        }
        catch (Exception e){
            log.error("Error guardando datos del plan: "+e.getMessage(),e);
        }
    }

