    @Override
    public void iniciaVista() {
        log.debug("iniciaVista()");
        // Aquí iniciaVista ha de recibir el objeto del Plan noivios        
        manejador = PlanNovioDatosAdicionales.inicia();
        
        reseteaFormulario();
        // Lectura de los datos del novio y la novia
        PlanNovioDatosAdicionales.estableceDatosNovios(l_nombre_novia, l_apellidos_novia, l_cedula_novia, l_nombre_novio, l_apellidos_novio, l_cedula_novio, lb_boda);

        if (manejador.getPlanNovio().getModo() == PlanNovioOBJ.MODO_MODIFICANDO_PLAN) {
            // MODO MODIFICANDO
            PlanNovioDatosAdicionales.estableceDatosAdicionales( t_lugar_boda, t_fecha_hora_boda, t_fecha_contacto_invitados, t_telefono, t_email);
            b_m_salvar.setText("<html><center>Guardar<br/>Cambios<br/> F2</center></html>");
            b_m_cancelar.setText("<html><center>Cancelar<br/> F3</center></html>");
            b_m_cancelar.setEnabled(true);

        }
        else {
            // MODO RESERVANDO
            b_m_salvar.setText("<html><center>Reservar <br/> F2</center></html>");
            b_m_cancelar.setText("");
            b_m_cancelar.setEnabled(false);
        }

        iniciaFoco();
    }

