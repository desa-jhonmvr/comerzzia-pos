    private void reseteaFormulario() {
        deshabilitaFormulario();
        t_lugar_boda.setText("");
        t_fecha_hora_boda.setText("");
        t_fecha_contacto_invitados.setText("");
        t_email.setText("");
        t_telefono.setText("");
        habilitaFormulario();
    }

    private void deshabilitaFormulario() {
        for(IValidableForm ivf: formulario){
            ivf.setValidacionHabilitada(false);
        }
    }
    private void habilitaFormulario() {
        for(IValidableForm ivf: formulario){
            ivf.setValidacionHabilitada(true);
        }
    }

