 public void inicializar() {
        
        ticketsAparcadosTableModel = new TicketsAparcadosTableModel();

        refrescarTablaPlanes();
        t_tickets.setColumnSelectionInterval(0,0);
        t_tickets.setRowSelectionInterval(0,0);
        t_tickets.requestFocus();
    }

    public void refrescarTablaPlanes() {
        t_tickets.setModel(ticketsAparcadosTableModel);
  

        t_tickets.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        t_tickets.getColumnModel().getColumn(0).setPreferredWidth(150);
        t_tickets.getColumnModel().getColumn(1).setPreferredWidth(272);
        
    }

    

    @Override
    protected void addHotKey(KeyStroke keyStroke, String inputActionKey, Action listener) {
        ActionMap actionMap = this.getActionMap();
        InputMap inputMap = this.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW);
        inputMap.put(keyStroke, inputActionKey);
        actionMap.put(inputActionKey, listener);
    }

    private void addFunctionKeys() {
        KeyStroke esc = KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0);
        Action listeneresc = new AbstractAction() {

            @Override
            public void actionPerformed(ActionEvent ae) {
                contenedor.setVisible(false);
            }
        };
        addHotKey(esc, "IdentClientesc", listeneresc);
        
        KeyStroke altmast=KeyStroke.getKeyStroke(KeyEvent.VK_T,KeyEvent.VK_ALT);
        Action listeneraltmast = new AbstractAction() {

            @Override
            public void actionPerformed(ActionEvent ae) {
                t_tickets.setColumnSelectionInterval(0,0);
                t_tickets.setRowSelectionInterval(0,0);
                t_tickets.requestFocus();
            }
        };
        addHotKey(altmast, "IdentClientaltmast", listeneraltmast);
    }
