    private void tb_listaFacturasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tb_listaFacturasKeyPressed
        if (evt.getKeyChar() == '\n') {
            accionSeleccionaFactura();
        } else if (evt.getKeyCode() == KeyEvent.VK_TAB) {
            b_volver.requestFocus();
        } else if (evt.getKeyCode() == KeyEvent.VK_F2) {
            accionIrAtras();
        }

    }//GEN-LAST:event_tb_listaFacturasKeyPressed

