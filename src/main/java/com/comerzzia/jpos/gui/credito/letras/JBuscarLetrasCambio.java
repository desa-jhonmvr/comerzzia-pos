    public void iniciaVista() {
        t_numero_factura.setValidacionHabilitada(false);
        manejador.inicia();
        limpiarFormulario();
        accionBuscarLetras();
        inicializaValidacion();
        refrescarTabla();
        iniciaFoco();
        t_numero_factura.setValidacionHabilitada(true);
    }

    public void recuperaVista() {
        refrescarTabla();
        lb_error.setText("");
        inicializaValidacion();
        iniciaFoco();
    }

    void iniciaFoco() {
        t_numero_factura.requestFocus();
    }

