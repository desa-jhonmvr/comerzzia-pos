    private void accionSeleccionaFactura() {
        try {
            manejador.accionSeleccionaFactura(tb_listaFacturas.getSelectedRow());
            accionIrADetalleDeFactura();
        } catch (LetraCambioException e) {
            lb_error.setText(e.getMessage());
            return;
        }
    }

    private void accionBuscarNumeroFactura() {
        try {
            t_numero_factura.validar();
            if (t_numero_factura.getText().isEmpty()) {
                lb_error.setText("Debe ingresar una caja.");
                return;
            }
//            if (t_numero_factura1.getText().isEmpty()) {
//                lb_error.setText("Debe ingresar un número de factura.");
//                return;
//            }           
            manejador.accionBuscarFactura(t_numero_factura1.getText(), t_numero_factura.getText());
            mostrarResultados();
        } catch (LetraCambioException e) {
            lb_error.setText(e.getMessage());
        } catch (ValidationFormException ex) {
            addError(ex);
        }
    }
    
     private void accionBuscarLetras() {
        try {
            manejador.accionBuscarLetras();
            mostrarResultados();
        } catch (LetraCambioException e) {
            lb_error.setText(e.getMessage());
        }
    }

    private void mostrarResultados() throws LetraCambioException {
//        if (manejador.getListaFacturas().isEmpty()) {
//          lb_error.setText("No existen letras de cambio para la datos seleccionados");
//          refrescarTabla();
//        } else 
        if (manejador.getListaFacturas().size() == 1) {
            manejador.accionSeleccionaFactura(0);
            accionIrADetalleDeFactura();
        } else {
            refrescarTabla();
        }

        if (manejador.getListaFacturas().size() > 1) {
            tb_listaFacturas.requestFocus();
        }
    }

    private void accionBuscarCedula() {
        lb_error.setText("");
        t_numero_factura1.setText("");
        t_numero_factura.setText("");

        if (t_numero_cedula.getText().isEmpty()) {
            lb_error.setText("Debe ingresar una Cedula");
            return;
        }
        try {
            manejador.accionBuscarCedula(t_numero_cedula.getText());
            mostrarResultados();
        } catch (LetraCambioException e) {
            lb_error.setText(e.getMessage());
        }
    }

    private void refrescarTabla() {
        tb_listaFacturas.setModel(new FacturasCambioTableModel(manejador.getListaFacturas()));
    }

    private void accionIrADetalleDeFactura() {
        contenedor.setVisible(false);
        JOperacionesTarjeta.crearVentanaPagarLetra();
        limpiarFormulario();

    }

    private void addFunctionsKeys() {
        KeyStroke f2 = KeyStroke.getKeyStroke(KeyEvent.VK_F2, 0);
        Action listenerf2 = new AbstractAction() {

            public void actionPerformed(ActionEvent ae) {

                b_volverActionPerformed(ae);
            }
        };
        addHotKey(f2, "IdentClientF12", listenerf2);
    }

    private void eliminaSeleccionTabla() {
        ListSelectionModel selectionModel = tb_listaFacturas.getSelectionModel();
        selectionModel.clearSelection();
    }

    private void accionIrAtras() {
        cerrarVentana();
        JPrincipal.getInstance().crearVentanaMenuTarjetas();
    }

    private void inicializaValidacion() {
        t_numero_factura.addValidador(new ValidadorTexto(3, true), this);
    }

    @Override
    public void addError(ValidationFormException e) {
        lb_error.setText(e.getMessage());
    }

    @Override
    public void clearError() {
        lb_error.setText("");
    }
