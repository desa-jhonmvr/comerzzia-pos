    @Override
    public void accionAceptar() {
        try {
            t_valor.validar();
            this.cantidad = new Integer(t_valor.getText());
            cerrarVentana();
        }
        catch (ValidationFormException ex) {
            addError(ex);
        }
        
    }

    @Override
    public void limpiarFormulario() {
        t_valor.setText("");
        lb_error.setText("");
    }

    @Override
    public void accionLeerTarjetaVD() {
        
    }

    @Override
    public void addError(ValidationFormException e) {
        lb_error.setText(e.getMessage());
    }

    @Override
    public void clearError() {
        lb_error.setText("");
    }

    public Integer getCantidad() {
        return cantidad;
    }

    public void setCantidad(Integer cantidad) {
        this.cantidad = cantidad;
        t_valor.setText(String.valueOf(cantidad));
    }
    
    public void iniciaFoco(){
        t_valor.requestFocus();
    }
