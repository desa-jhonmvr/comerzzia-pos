      @Override
  public void accionAceptar() {
        try {
            t_valor.validar();
        } catch (ValidationFormException ex) {
            java.util.logging.Logger.getLogger(JIngresarGarantia.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!t_valor.getText().isEmpty()) {
            if (getTipoValor() == TIPO_CADENA) {
                valorS = t_valor.getText();
                usuario = UsuariosDao.obtenerUsuarioporNumero(valorS);
                
            } else if (getTipoValor() == TIPO_NUMERICO || getTipoValor() == TIPO_DECIMAL) {
                valor = new BigDecimal(t_valor.getText());
            }
            if (usuario != null) {
                contenedor.setVisible(false);
            } else {
                lb_error.setText("Usuario no encontrado");
            }
        } else {
            usuario=null;
            lb_error.setText("Debe ingresar un valor");
        }
    }

    @Override
    public void limpiarFormulario() {
        setTipoValor(TIPO_CADENA);
        valor = new BigDecimal(-1);
        valorS = null;
        valorMaximo = null;
        lb_error.setText("");
        t_valor.setText("");
        lb_mensaje.setText("");
    }

