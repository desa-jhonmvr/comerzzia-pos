    public int getTipoValor() {
        return tipoValor;
    }

    public void setTipoValor(int tipoValor) {
        this.tipoValor = tipoValor;
        t_valor.removeValidadores();
        if (tipoValor == TIPO_NUMERICO) {
            t_valor.addValidador(new ValidadorEntero(0, valorMaximo.intValue()), this);
        }
        else if (tipoValor == TIPO_DECIMAL) {
            t_valor.addValidador(new ValidadorDecimal(valorMaximo, BigDecimal.ZERO, 2), this);
        }

    }

