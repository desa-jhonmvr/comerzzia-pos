    private void accionCancelar() {
       //deshabilitamos validación porque podría pasar qe no dejase ir el foco de un campo requerido
     deshabilitarFormulario();
        if (JPrincipal.getInstance().crearVentanaConfirmacion("¿Desea cancelar la creación de guía de remisión?")){
            JPrincipal.getInstance().showViewMenuVentas();
       }
        else{
            habilitarFormulario();
        }
    }

    private void deshabilitarFormulario() {
        for(IValidableForm ivf:formulario){
            ivf.setValidacionHabilitada(false);
        }
    }
    
    private void habilitarFormulario() {
        for(IValidableForm ivf:formulario){
            ivf.setValidacionHabilitada(true);
        }
    }
