    @Override
    public void iniciaVista() {
        try {
            // iniciar foco
            t_fecha_inicioTraslado.requestFocus();
            
            cargarDatosFormulario();
            iniciaFoco();
            habilitarFormulario();
            iniciaFoco();
        }
        catch (ValidationFormException ex) {
            accionCancelar();
            JPrincipal.getInstance().crearError(ex.getMessage());
        }

    }

    private void accionPasoSiguiente() {
        try {
            // Validar los datos del formulario               
            for (IValidableForm val : formulario) {
                val.validar();
            }
            
            int indiceSeleccionado = 0;
            
            if (jRadioButton3.isSelected()){
                indiceSeleccionado = 10;
            }
            else if (jRadioButton4.isSelected()){
                indiceSeleccionado = 13;
            }
            else if (jRadioButton7.isSelected()){
                indiceSeleccionado = 61;
            }
            else if (jRadioButton1.isSelected()){
                indiceSeleccionado = 1;
            }

            //Una de las formas en que será posible pasar a la siguiente pantalla es habiéndo ingresado una factura
            manejador.limpiarDatosGR();
            
            if (jRadioButton1.isSelected()) {
                if (t_documentoImpreso.getText().isEmpty()) {
                    addError(new ValidationFormException("No ha introducido un número de documento"));
                    t_documentoImpreso.requestFocus();
                }
                else {  
                    manejador.setParametrosGuiaRemision(t_fecha_inicioTraslado.getText(),
                            t_fecha_fin_traslado.getText(),
                            t_documentoImpreso.getText(),
                            t_codigo_local1.getText(),
                            t_codigo_local2.getText(),
                            t_numero_orden_despacho.getText(),
                            t_numero_orden_salida.getText(),
                            t_numero_pedido.getText(),
                            t_numero_transferencia.getText(),
                            indiceSeleccionado
                            );
                            
                    // Consultamos si hay un ticket con lineas de envío a domicilio
                    // Si no hay ticket o no hay lineas devolveremos sendas excepciones
                    //TODO ¿Comprobamos que el ticket este en BD?    
                    manejador.consultaTicket(); // este método ya cambia la pantalla si tiene exito
                }
            }
            else{
                manejador.reseteaListaLineas();
                //TODO [GUIAS REMISION] : Controlar validaciones de campos requeridos para otros tipos de documentos
                // Actualmente no se podran crear estas guias porque debe haber por lo menos una línea seleccionada.
                
               
                manejador.setParametrosGuiaRemision(t_fecha_inicioTraslado.getText(),
                            t_fecha_fin_traslado.getText(),
                            t_documentoImpreso.getText(),
                            t_codigo_local1.getText(),
                            t_codigo_local2.getText(),
                            t_numero_orden_despacho.getText(),
                            t_numero_orden_salida.getText(),
                            t_numero_pedido.getText(),
                            t_numero_transferencia.getText(),
                            indiceSeleccionado);
                JPrincipal.getInstance().irPantallaGuiaRemision2();
            }

        }
        catch (ValidationFormException ex) {
            addError(ex);
        }
        // Se consultará el ticket para saber que artículos serán los que vamos a trasladar
    }

    private void inicializarValidacion() {
        formulario = new ArrayList();

        Fecha fechaMin = new Fecha();
        fechaMin.sumaDias(-1);
        
        t_fecha_inicioTraslado.addValidador(new ValidadorFecha(fechaMin.getDate(), null), this);
        t_fecha_fin_traslado.addValidador(new ValidadorFecha(fechaMin.getDate(), null), this);
        t_fecha_inicioTraslado.addValidador(new ValidadorObligatoriedad(), this);
        t_fecha_fin_traslado.addValidador(new ValidadorObligatoriedad(), this);
              
        //Validadores de longitud
        t_documentoImpreso.addValidador(new ValidadorTexto(20), this);
        t_numero_orden_despacho.addValidador(new ValidadorTexto(10), this);
        t_numero_orden_salida.addValidador(new ValidadorTexto(10), this);
        t_numero_pedido.addValidador(new ValidadorTexto(10), this);
        t_numero_transferencia.addValidador(new ValidadorTexto(10), this);
        t_codigo_local2.addValidador(new ValidadorTexto(3), this);
        t_codigo_local1.addValidador(new ValidadorTexto(3), this);
          
        formulario.add(t_fecha_inicioTraslado);
        formulario.add(t_fecha_fin_traslado);
        formulario.add(t_documentoImpreso);
        formulario.add(t_numero_orden_despacho);
        formulario.add(t_numero_orden_salida);
        formulario.add(t_numero_pedido);
        formulario.add(t_numero_transferencia);
        formulario.add(t_codigo_local2);
        formulario.add(t_codigo_local1);
    }

