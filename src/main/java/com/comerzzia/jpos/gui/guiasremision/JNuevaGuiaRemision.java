    private void cargarDatosFormulario() throws ValidationFormException {
        if (!GuiasRemisionManager.isAccionVolver()) {
            // limpiamos el formulario
            limpiarFormulario();
        }
        else {
            // recuperamos el formulario
            recuperarFormulario();

        }
    }

    private void limpiarFormulario() throws ValidationFormException {
        manejador.establecerCodigoGR();
        
        t_fecha_inicioTraslado.setText("");
        t_fecha_fin_traslado.setText("");
        t_numero_orden_despacho.setText("");
        t_numero_orden_salida.setText("");
        t_numero_pedido.setText("");
        t_numero_transferencia.setText("");
        t_codigo_local2.setText("");
        t_codigo_local1.setText("");
        t_documentoImpreso.setText("");
        jRadioButton1.setSelected(true);
        l_codigo.setText(manejador.getIdentificador());
    }

    private void recuperarFormulario() {
        t_fecha_inicioTraslado.setText(manejador.getFechaInicioTrasladoStr());
        t_fecha_fin_traslado.setText(manejador.getFechaFinTrasladoStr());
 /*       t_numero_orden_despacho.setText(manejador.getNumeroOrdenDespacho());
        t_numero_orden_salida.setText(manejador.getNumeroOrdenSalida());
        t_numero_pedido.setText(manejador.getNumeroPedido());
        t_numero_transferencia.setText(manejador.getNumeroTransferencia());*/
        t_codigo_local1.setText(manejador.getCodigoLocal1());
        t_codigo_local2.setText(manejador.getCodigoLocal2());
        t_documentoImpreso.setText(manejador.getNumeroDocumentoImpreso());
        l_codigo.setText(manejador.getIdentificador());
        switch (manejador.getGuiaRemision().getTipoDocumento()) {
            case (1):
                 jRadioButton1.setSelected(true);
                break;            
            case (10):
                 jRadioButton3.setSelected(true);
                break;
            case (13):
                 jRadioButton4.setSelected(true);
                 break; 
            case (61):
                 jRadioButton7.setSelected(true);
                 break;
            default:
                ;
        }
        GuiasRemisionManager.setAccionVolver(false);
        
    }

