    public void iniciaVista(){    
        limpiarFormulario();  
        
        try{
            manejador.accionConsultarStockTotal();
            refrescaTabla(true);
        }
        catch (StockException e){
            log.error(e.getMessage());
        }
        catch(Exception e){
            log.error("No se pudo consultar el Stock");
        }
        
        lb_articulo.setText(manejador.getNombreArticulo());
    }

    private void refrescaTabla(boolean mostrarBloqueo) {
        tb_listaStock.setModel(new StockTableModel(manejador.getListaStock(), mostrarBloqueo));
    }

    public void setArticuloBuscado(Articulos articulo) {
        manejador.setArticuloBuscado(articulo);
    }

    public void setArticuloBuscado(Vector vectorArt) {
        log.debug("setArticuloBuscado() - establecemos el articulo buscado");
        Articulos art = new Articulos();
        art.setCodart((String)vectorArt.get(0));
        art.setDesart((String)vectorArt.get(1));
        manejador.setArticuloBuscado(art);
    }

    private void accionOtrosLocales() {
        contenedor.setVisible(false);
        try{
            lb_error.setText("");   
            // Pedimos autorizaci√≥n
            String codAdministrador = JPrincipal.getInstance().compruebaAutorizacion(Operaciones.STOCK, "Necesitas permisos para ver el stock");
            manejador.accionConsultaStockOtrosLocales();
            refrescaTabla(false);
        }
        catch (StockException e){
            log.error(e.getMessage());
        }
        catch (SinPermisosException ex) {
            log.debug("No se tiene permisos para a√±adir el art√≠culo con falta de stock.");
        }
        catch(Exception e){
            log.error("No se pudo consultar el Stock");
        }
        contenedor.setVisible(true);
    }

