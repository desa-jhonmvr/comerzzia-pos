    @Override
    public void accionAceptar() {
        try {
            BigDecimal importe = new BigDecimal(this.t_saldo_inicial.getText());
            if (importe.compareTo(BigDecimal.ZERO) < 0) {
                throw new ValidationException("El valor de apertura de caja ha de ser positivo");
            }
            Sesion.aperturaDeCaja(importe);
            cerrarVentana();
        }
        catch (NumberFormatException e) {
            log.debug("Error de formato, se precisa un número");
            this.getVentana_padre().crearError("Debe introducir un importe válido");
        }
        catch (ValidationException e) {
            log.debug("Error de validación en inserción");
            this.getVentana_padre().crearError(e.getMessage());
        }
        catch (Exception e) {
            log.error("No se pudo realizar la operación de apertura de caja", e);
            this.getVentana_padre().crearError("No se pudo realizar la operación de apertura de caja");
        }
    }

