    private void accionAmpliarRetiro() {
        log.debug("Cajas- ACCION AMPLIAR RETIRO");
        try {
            Sesion.getCajaActual().crearApunteAmpliarRetiro();

            //Sesion.cajaActual.setEfectivoEnCaja(Sesion.cajaActual.getEfectivoEnCaja().add(Sesion.config.getLimiteRetiro()));
            Sesion.cajaActual.setLimiteRetiro(Sesion.cajaActual.getEfectivoEnCaja().add(Sesion.config.getLimiteRetiro()));
            PrintServices.getInstance().imprimirComprobanteRetiro(t_cantidad_retiro.getText(),(new Fecha()).getStringHora(),Sesion.getUsuario());
            cerrarVentana();
            this.getVentana_padre().crearConfirmacion("El retiro se realiz贸 satisfactoriamente");

        }
        catch (NumberFormatException e){
            log.debug("Valor de retiro no valido");
            t_cantidad_retiro.requestFocus();
            this.getVentana_padre().crearError("El valor de retiro no es una cantidad valida");
        }
        catch (Exception e) {
            log.debug("Error en la creaci贸n del Apunte de Ampliaci贸n de Retiro");
            this.getVentana_padre().crearError("Error en la realizaci贸n del retiro");
        }
    }
        
