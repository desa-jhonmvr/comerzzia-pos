package com.comerzzia.jpos.dto.ventas;

/**
 *
 * @author Gabriel Simbania
 */
public class DatosCiudadanoDTO {

    private String apellidos;
    private String nombres;
    private String calle;
    private String domicilio;
    private String estadoCivil;
    private String genero;
    private String codigoTipoCedula;
    private String identificacion;
    private String nacionalidad;
    private String nuiConyuge;

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getCalle() {
        return calle;
    }

    public void setCalle(String calle) {
        this.calle = calle;
    }

    public String getDomicilio() {
        return domicilio;
    }

    public void setDomicilio(String domicilio) {
        this.domicilio = domicilio;
    }

    public String getEstadoCivil() {
        return estadoCivil;
    }

    public void setEstadoCivil(String estadoCivil) {
        this.estadoCivil = estadoCivil;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getCodigoTipoCedula() {
        return codigoTipoCedula;
    }

    public void setCodigoTipoCedula(String codigoTipoCedula) {
        this.codigoTipoCedula = codigoTipoCedula;
    }

    public String getIdentificacion() {
        return identificacion;
    }

    public void setIdentificacion(String identificacion) {
        this.identificacion = identificacion;
    }

    public String getNacionalidad() {
        return nacionalidad;
    }

    public void setNacionalidad(String nacionalidad) {
        this.nacionalidad = nacionalidad;
    }

    public String getNuiConyuge() {
        return nuiConyuge;
    }

    public void setNuiConyuge(String nuiConyuge) {
        this.nuiConyuge = nuiConyuge;
    }
    
    

}
